function dropDown(id) {
    let acOdian = document.querySelector(`#${id}`)
    //console.log(acOdian);
    if (acOdian.style.display === 'block') {
        acOdian.style.display = 'none'
    } else {
        acOdian.style.display = 'block'
    }
}

